from fastapi import APIRouter, Depends, status
from sqlalchemy.ext.asyncio import AsyncSession
from sqlalchemy import select
# Importing our schemas, models, and database dependency
from schemas.client_schema import ClientCreate, ClientResponse
from models.client import Client
from core.database import get_db

# 1. Creating a Router for client-related endpoints
router = APIRouter(prefix="/clients", tags=["Clients"])

# 2. POST endpoint to create a new client
@router.post("/", response_model=ClientResponse, status_code=status.HTTP_201_CREATED)
async def create_client(
    client_data: ClientCreate, 
    db: AsyncSession = Depends(get_db)
):
    # Step A: Convert Pydantic schema to a Python dictionary and unpack it
    new_client = Client(**client_data.model_dump())
    
    # Step B: Add the new object to the database session
    db.add(new_client)
    
    # Step C: Save changes to the database (Commit transaction)
    await db.commit()
    
    # Step D: Refresh the object to get the ID generated by PostgreSQL
    await db.refresh(new_client)
    
    return new_client

@router.get("/", response_model=list[ClientResponse])
async def get_clients(db: AsyncSession = Depends(get_db)):
    # Выполняем SQL-запрос: SELECT * FROM clients;
    result = await db.execute(select(Client))
    
    # Извлекаем все найденные записи из результата
    clients = result.scalars().all()
    
    return clients    

